def _strict_json_contract() -> str:
    return (
        "Strict JSON contract for the `.json` file:\n"
        + "- Write valid JSON only; do not wrap it in markdown fences.\n"
        + "- Use UTF-8 without BOM.\n"
        + "- Use ASCII-only string values in JSON. Put Chinese prose in the Markdown file, not JSON.\n"
        + "- Escape all quotes and newlines inside strings.\n"
        + "- Do not use comments, trailing commas, or partially written strings.\n\n"
    )


def _business_report_posture() -> str:
    return (
        "Business-report posture:\n"
        + "- Internal evidence artifacts are guardrails, not the reader-facing story.\n"
        + "- The final package must feel like a management report, not an audit memo.\n"
        + "- Prioritize executive thesis, quantified diagnosis, operating choices, commercial implications, trade-offs, owners, KPIs, and 30/60/90 day actions.\n"
        + "- Use evidence limits to calibrate wording internally; do not let caveats dominate the prose.\n"
        + "- Avoid reader-facing sections titled `evidence boundary`, `unsupported claims`, `weak evidence`, `limitations`, `证据边界`, `不支持的结论`, or `弱证据`.\n"
        + "- If scope must be mentioned, compress it into one short reading note and immediately return to business decisions.\n\n"
    )


def _generic_report_lens(context_payload: dict[str, Any] | None = None) -> str:
    """Return only the explicit management-posture flag for generic pipelines.

    Non-management generic long reports no longer use a fixed backend lens.
    Their reader angle is generated by the runtime smart-lens stage for each
    dataset; this helper is kept only for backward-compatible business branches.
    """
    payload = context_payload or {}
    if bool(payload.get("enable_generic_business_runtime") or payload.get("generate_generic_business_runtime")):
        return "business_management"
    nested_request = payload.get("request")
    if isinstance(nested_request, dict) and bool(
        nested_request.get("enable_generic_business_runtime")
        or nested_request.get("generate_generic_business_runtime")
    ):
        return "business_management"
    explicit = str(payload.get("report_lens") or payload.get("generic_report_lens") or "").strip().lower()
    if explicit in {"business_management", "management", "business"}:
        return "business_management"
    if explicit in {"generic_analytical", "analytical", "data_analysis", "data_native", "smart_lens"}:
        return "smart_lens"
    if str(payload.get("specialized_mode") or "").strip():
        return "business_management"

    intent_parts: list[str] = []
    for key in (
        "user_requirement",
        "original_requirement",
        "optimized_user_requirement",
        "report_goal",
        "target_audience",
        "analysis_mode",
        "business_focus",
        "core_purpose",
        "expected_result",
        "recommendation_style",
        "report_type",
    ):
        value = payload.get(key)
        if value:
            intent_parts.append(str(value))
    for nested_key in ("requirement_intent_json", "multi_requirement_intent_json"):
        nested = payload.get(nested_key)
        if isinstance(nested, dict):
            for key in (
                "optimized_user_requirement",
                "target_audience",
                "core_purpose",
                "expected_result",
                "recommendation_style",
                "report_type",
            ):
                value = nested.get(key)
                if value:
                    intent_parts.append(str(value))
    intent_text = " ".join(intent_parts).lower()
    business_markers = (
        "management",
        "business",
        "executive",
        "decision",
        "recommendation",
        "action plan",
        "\u7ecf\u8425",
        "\u7ba1\u7406",
        "\u7ba1\u7406\u7248",
        "\u4e1a\u52a1",
        "\u5546\u4e1a",
        "\u884c\u52a8",
        "\u5efa\u8bae",
        "\u8d1f\u8d23\u4eba",
        "owner",
        "checkpoint",
    )
    return "business_management" if any(marker in intent_text for marker in business_markers) else "smart_lens"


def _generic_is_business_management(context_payload: dict[str, Any] | None = None) -> bool:
    return _generic_report_lens(context_payload) == "business_management"


